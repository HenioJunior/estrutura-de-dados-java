public class Node {
    private int elemento;
    private Node proximo;

    public int getElemento() {
        return elemento;
    }

    public Node getProximo() {
        return proximo;
    }

    public void setElemento(int elemento) {
        this.elemento = elemento;
    }

    public void setProximo(Node proximo) {
        this.proximo = proximo;
    }
}
